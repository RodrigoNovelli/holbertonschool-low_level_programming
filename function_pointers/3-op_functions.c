#include <stdlib.h>
#include "calc.h"
/**
 * op_add - adds two ints
 * @a: first addend
 * @b: second addend
 * Return: the total of a plus b, int type.
 */
int op_add(int a, int b)
{
	int r;

	r = a + b;
	return (r);
}
/**
 * op_sub - substracts two ints
 * @a: the minuend
 * @b: the subtrahend
 * Return: the difference between a and b, int data type
 */
int op_sub(int a, int b)
{
	int r;

	r = a - r;
	return (r);
}
/**
 * op_mul - multiplies two ints
 * @a: the first factor
 * @b: the second factor
 * Return: The product between a and b (int).
 */
int op_mul(int a, int b)
{
	int r;

	r = a * r;
	return (r);
}
/**
 * op_div - divides two numbers
 * @a: the dvisor
 * @b: the dividend
 * Return: quotient between a and b, int type.
 */
int op_div(int a, int b)
{
	int r;

	r = a / r;
	return (r);
}
/**
 * op_mod - function that returns the remainder between
 * two ints.
 *   */
int op_mod(int a, int b)
{
	int r;

	r = a % b;
	return (r);
}
